            Radisson Hotel Group expanded its partnership with SM Hotels & Conventions Corp. and signed a Master Development Agreement to reach 20 hotels in the Philippines by 2028.
            Radisson Hotel Group signed an agreement with La Vie Hotels & Resorts to add over 30 hotels to Group’s portfolio in APAC.
            Radisson Hotel Group and PPHE Hotel Group accelerated the global growth of the premium lifestyle art’otel brand, with the inclusion of art’otel in the Group’s portfolio of bold, art and design-inspired hotels, growing the portfolio to ten distinct brands under on overarching umbrella.
