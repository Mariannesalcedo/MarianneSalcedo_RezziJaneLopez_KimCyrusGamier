        <h2>Our History</h2>
        <h3>Our history summarized in a timeline</h3>
        <p>At Radisson Hotel Group we never stop searching for new ways to serve our guests, provide opportunities for our owners and business partners and become leaders. Our company, that began in Copenhagen, Denmark with the launch of the world’s first designer hotel, has grown to span the world and continues to offer new locations every year. Let's look back and get to know our history a little better.</p>
        <h3>1960-2023</h3>
        
            <h3>1960</h3>
            <p>First SAS hotels opened in Copenhagen, the current Radisson Collection Royal Hotel, Copenhagen.</p>

            <h3>1962</h3> 
            <p>Group name changed to Carlson and acquired Minneapolis Radisson Hotels.</p>

            <h3>1986</h3>
            <p>Carlson Hotels and SAS International Hotels signed a partnership to bring the Radisson brand to Europe.</p>

            <h3>1987 </h3>
            <p>Carlson Hotels launched the Country Inns & Suites by Carlson brand.</p>

            <h3>1989 </h3>
            <p>Headquarters moved to Brussels</p>

            <h3>1994 </h3>
            <p>SAS International Hotels signed a franchise agreement with Carlson Hotels to manage the Radisson brand in EMEA, which would later become Radisson Blu.</p>

            <h3>1999 </h3>
            <p>Carlson Hotels launched the guest loyalty program now known as Radisson Rewards.</p>

            <h3>2002 </h3>
            <p>SAS International Hotels became The Rezidor Hotel Group and acquired franchise rights to Regent Hotels & Resorts, Park Inn and Country Inns & Suites By Carlson.</p>

            <h3>2006</h3>
            <p>The Rezidor Hotel Group completed an initial public offering on the Stockholm Stock Exchange. Carlson purchased additional shares, increasing its ownership to 35% 2009 Radisson SAS brand name was updated to Radisson Blu.</p>

            <h3>2010 </h3>
            <p>Radisson Blu became the largest Upscale Hotel Brand in Europe, per MKG Park Inn becomes Park Inn by Radisson Rezidor named one of the Most Ethical Companies of the World by Ethisphere Institute.</p>

            <h3>2012 </h3>
            <p>Carlson increased its ownership in The Rezidor Hotel Group to 51%, and the two joined forces in a strategic partnership and went to market as Carlson Rezidor Hotel Group. 
            Carlson Rezidor Hotel Group was named Hotel Group of the Year at the Worldwide Hospitality Awards 2012.</p>

            <h3>2014 </h3>
            <p>Carlson Rezidor Hotel Group announced a new brand in the upscale lifestyle select segment, Radisson RED</p>

            <h3>2016 </h3>
            <p>HNA Tourism Group acquired Carlson Hotels, Inc. 
            The Rezidor Hotel Group entered the economy segment with a 49% stake in prizeotel.</p>

            <h3>2017 </h3>

            <p>Federico J. González became Rezidor’s President & CEO and Chairman of the Global Steering Committee.
            HNA Tourism Group Co., Ltd. announced a mandatory public offer to acquire all shares in Rezidor.</p>

            <h3>2018 </h3>
            <p>Country Inns & Suites by Carlson became Country Inn & Suites by Radisson. 
            Carlson Rezidor Hotel Group became Radisson Hotel Group, a new global brand architecture, introducing Radisson in EMEA and launching the Radisson Collection brand.
            Radisson Hotel Group rebranded Club Carlson loyalty program to Radisson Rewards Rezidor Hotel Group AB (publ) renamed to Radisson Hospitality AB (publ) on 7 May 2018.</p>

            <h3>2019</h3> 
            <p>A consortium led by Jin Jiang International Holdings Co. Ltd. completed the acquisition of the shares in Radisson Hospitality AB.
            Radisson Hotel Group began the roll-out of its new global Responsible Business training program.
            Radisson Hotel Group entered the future of digital hospitality with the launch of its new multi-brand platform: RadissonHotels.com.
            The first co-branded hotel of Jin Jiang International and Radisson Hotel Group was launched, Radisson Blu Hotel, Frankfurt.
            Radisson Hospitality AB completed the acquisition of prizeotel.
            Radisson Hospitality AB’s application for de-listing from Nasdaq Stockholm was approved.</p>

            <h3>2020</h3>
            <p>In response to the COVID-19 pandemic, Radisson Hotel Group announced its Radisson Safety Protocol, a global commitment to cleanliness and hygiene in partnership with SGS
            Radisson Hotel Group launched a new brand, Radisson Individuals.
            Radisson Hotel Group strengthened its extended stay value proposition and started to grow its serviced apartments portfolio across EMEA. </p>

            <h3>2021 </h3>
            <p>Radisson Hotel Group renewed its partnership with SGS and continued global application of the Radisson Hotels Safety Protocol.
            Radisson Hotel Group and Allego partnered to develop pan-European charging network and infrastructure for electric vehicles and green mobility.
            Radisson Hotel Group set up a collaboration with Amadeus to integrate its Traveler ID platform into the Group’s check-in web portal, making seamless travel and contactless check-in possible for guest.
            Radisson Collection launched its first global art series with emerging artists to create unique art pieces inspired by each hotel’s iconic location.
            Radisson Hotel Group became the world’s first hotel group to offer 100% carbon negative meetings.</p>

            <h3>2022 </h3>
            <p>Radisson Hotel Group set new targets and commitments to decarbonize its business with ambitious emission reduction targets to be net zero by 2050, with approved and validated Science Based Targets, in line with the SBTi Net-Zero Standard.
            Together with top brands, associations and destinations in the industry representing over 50,000 hotels, Radisson Hotel Group launched the Hotel Sustainability Basics, including the definition of 12 criteria and actions, which all hotels should implement as a minimum on their road to sustainability. 
            Radisson Academy’s third anniversary was celebrated with 1.5M hours spent in training and development.
            Radisson Hotel Group sold the franchise business, operations, and intellectual property of Radisson Hotels Americas to Choice Hotels International 
            Radisson Hotel Group announced its Asia Pacific Expansion Plan including the establishment of new dedicated Business Units in Thailand, Vietnam, Indonesia and Australasia.
            Radisson Rewards relaunched its loyalty program offering the fastest route to top tier in the industry, new benefits, and the opportunity to make their stays carbon compensated, making green stays easier.
            Radisson Hotel Group launched a new midscale brand, Park Inn & Suites by Radisson in India and signed a strategic partnership with Ruptub Solutions to add 150 hotels in India.
            Radisson Hotel Group signed the Glasgow Declaration and reaffirmed its strong sustainability commitment during COP27 as well as joining the Global Sustainable Tourism Council (GSTC).
            Radisson Hotel Group ranked fourth in Forbes Best Employer ranking in the travel and leisure category globally for the second consecutive year. </p>
            
